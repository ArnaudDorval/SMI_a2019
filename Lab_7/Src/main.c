void Convert_FFT_To_Matrix(float32_t *outputFFT, float32_t maxFFT) {

	float32_t maximum[MATRIX_WIDTH];
	int index = 0;

	// On trouve la valeur max des 6 plages de fréquences
	for(int i = 1; i < SAMPLES/2; i++) {
		if(i != 0 && i%(SAMPLES/2/MATRIX_WIDTH) == 0) {
			index++;
		}
		// On garde la valeur maximale trouvée dans chaque plage de fréquences
		if(outputFFT[i] > maximum[index]) {
			maximum[index] = outputFFT[i];
		}
	}

	// On détermine la hauteur que devrait avoir chaque colonne
	uint8_t heights[MATRIX_WIDTH];
	for(int i = 0; i < MATRIX_WIDTH; i++) {
		if (i == 0){
			// Valeur 250000 déterminée analytiquement
			heights[i] = round(maximum[i]/250000 * MATRIX_HEIGHT);
		} else if (i == 1){
			heights[i] = round(maximum[i]/250000 * MATRIX_HEIGHT);
		} else{
			heights[i] = round(maximum[i]/200000 * MATRIX_HEIGHT);
		}

	}

	uint8_t temp = 0;

	for(int i = 0; i < MATRIX_HEIGHT; i++) {
		for(int j = 0; j <= MATRIX_WIDTH; j++) {
			if (i < heights[j]){
				// On place un 1 à la position j de la colonne
				temp |= (0x01 << ( MATRIX_HEIGHT - 1 - j));
			}else{
				// On place un 0 à la position j de la colonne
				temp &= ~(0x01 << ( MATRIX_HEIGHT - 1 - j));
			}

		}

		// On change la valeur de la colonne de la matrice de LED
		setCol_MAX7219(&hspi1, i + 1, temp);
	}

}

